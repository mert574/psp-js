// KIRK_CMD1_HEADER offsets
const CMD1_MODE = 0x60;
const CMD1_ECDSA_HASH = 0x64;
const CMD1_DATA_SIZE = 0x70;
const CMD1_DATA_OFFSET = 0x74;
const HEADER_SIZE = 0x90;

const KIRK_MODE_CMD1 = 1;

function readU32LE(buf: Uint8Array, off: number): number {
  return buf[off] | (buf[off+1] << 8) | (buf[off+2] << 16) | ((buf[off+3] << 24) >>> 0);
}

/**
 * kirkCMD1: Decrypt private (KIRK command 1).
 * Input: buffer starting with 0x90-byte KIRK_CMD1_HEADER followed by encrypted data.
 * Returns decrypted payload.
 */
export async function kirkCMD1(inbuf: Uint8Array): Promise<Uint8Array> {
  if (inbuf.length < HEADER_SIZE) throw new Error('kirkCMD1: buffer too small');

  const header = inbuf.slice(0, HEADER_SIZE);
  const mode = readU32LE(header, CMD1_MODE);
  const ecdsaHash = header[CMD1_ECDSA_HASH];
  const dataSize = readU32LE(header, CMD1_DATA_SIZE);
  const dataOffset = readU32LE(header, CMD1_DATA_OFFSET);

  // Decrypt the AES and CMAC keys from header[0..0x1F] using kirk1_key
  const decryptedKeys = await aesCbcDecrypt(header.slice(0, 0x20), kirk1_key);
  const aesKey = decryptedKeys.slice(0, 16);
  const cmacKey = decryptedKeys.slice(16, 32);

  // If mode is KIRK_MODE_CMD1, verify CMAC
  if (mode === KIRK_MODE_CMD1) {
    // Verify header CMAC (over header[0x60..0x90])
    const headerCmac = await aesCmac(header.slice(0x60, HEADER_SIZE), cmacKey);
    const expectedHeaderCmac = header.slice(0x20, 0x30);
    if (!headerCmac.every((b, i) => b === expectedHeaderCmac[i])) {
      throw new Error('kirkCMD1: header CMAC verification failed');
    }

    // Verify data CMAC — covers header[0x60..0x90] + gap + aligned data
    // Matches PPSSPP: AES_CMAC(key, inbuff+0x60, 0x30 + ALIGN16(data_size) + data_offset)
    let chkSize = dataSize;
    if (chkSize % 16) chkSize += 16 - (chkSize % 16);
    const dataCmacLen = 0x30 + chkSize + dataOffset;
    if (0x60 + dataCmacLen > inbuf.length) throw new Error('kirkCMD1: data extends past buffer');
    const dataCmac = await aesCmac(inbuf.slice(0x60, 0x60 + dataCmacLen), cmacKey);
    const expectedDataCmac = header.slice(0x30, 0x40);
    if (!dataCmac.every((b, i) => b === expectedDataCmac[i])) {
      throw new Error('kirkCMD1: data CMAC verification failed');
    }
  }

  // Decrypt the payload
  const dataStart = HEADER_SIZE + dataOffset;
  const dataEnd = dataStart + dataSize;
  if (dataEnd > inbuf.length) throw new Error('kirkCMD1: data extends past buffer');
  return aesCbcDecrypt(inbuf.slice(dataStart, dataEnd), aesKey);
}

/**
 * kirkCMD10: CMAC verification only (KIRK command 10).
 * Returns true if verification passes.
 */
export async function kirkCMD10(inbuf: Uint8Array): Promise<boolean> {
  if (inbuf.length < HEADER_SIZE) return false;

  const header = inbuf.slice(0, HEADER_SIZE);
  const dataSize = readU32LE(header, CMD1_DATA_SIZE);
  const dataOffset = readU32LE(header, CMD1_DATA_OFFSET);

  // Decrypt keys
  const decryptedKeys = await aesCbcDecrypt(header.slice(0, 0x20), kirk1_key);
  const cmacKey = decryptedKeys.slice(16, 32);

  // Verify header CMAC
  const headerCmac = await aesCmac(header.slice(0x60, HEADER_SIZE), cmacKey);
  const expectedHeaderCmac = header.slice(0x20, 0x30);
  if (!headerCmac.every((b, i) => b === expectedHeaderCmac[i])) return false;

  // Verify data CMAC — covers header[0x60..] + gap + aligned data
  let chkSize = dataSize;
  if (chkSize % 16) chkSize += 16 - (chkSize % 16);
  const dataCmacLen = 0x30 + chkSize + dataOffset;
  if (0x60 + dataCmacLen > inbuf.length) return false;
  const dataCmac = await aesCmac(inbuf.slice(0x60, 0x60 + dataCmacLen), cmacKey);
  const expectedDataCmac = header.slice(0x30, 0x40);
  return dataCmac.every((b, i) => b === expectedDataCmac[i]);
}
